const fs = require('fs');
const path = require('path');

// Build a fully static Docsify site with embedded content
function buildStatic() {
  const docsDir = __dirname;
  const outputFile = path.join(docsDir, 'index.html');

  // Read all markdown files
  const files = {};

  fs.readdirSync(docsDir).forEach(file => {
    if (file.endsWith('.md') && file !== '_sidebar.md') {
      const content = fs.readFileSync(path.join(docsDir, file), 'utf8');
      files[file] = content;
    }
  });

  // Build search index
  const searchIndex = buildSearchIndex(files);

  // Generate static HTML
  const html = generateStaticHTML(files, searchIndex);

  fs.writeFileSync(outputFile, html, 'utf8');

  console.log('‚úÖ Static documentation generated:', outputFile);
  console.log(`üìÑ ${Object.keys(files).length} markdown files embedded`);
  console.log(`üîç ${searchIndex.length} search index entries`);
}

function extractTitle(content) {
  const match = content.match(/^#\s+(.+)$/m);
  return match ? match[1].trim() : 'Untitled';
}

function buildSearchIndex(files) {
  const index = [];

  Object.entries(files).forEach(([fileName, content]) => {
    const lines = content.split('\n');
    let currentSection = extractTitle(content);

    lines.forEach(line => {
      const trimmed = line.trim();

      // Skip code blocks
      if (trimmed.startsWith('```')) return;

      // Extract headings
      const h1 = trimmed.match(/^#\s+(.+)$/);
      const h2 = trimmed.match(/^##\s+(.+)$/);
      const h3 = trimmed.match(/^###\s+(.+)$/);

      if (h1) {
        currentSection = h1[1];
        index.push({
          title: currentSection,
          heading: currentSection,
          text: trimmed,
          url: `#/${fileName}`,
          level: 1
        });
      } else if (h2) {
        const heading = h2[1];
        index.push({
          title: `${currentSection} - ${heading}`,
          heading: heading,
          text: trimmed,
          url: `#/${fileName}?id=${slugify(heading)}`,
          level: 2
        });
      } else if (h3) {
        const heading = h3[1];
        index.push({
          title: `${currentSection} - ${heading}`,
          heading: heading,
          text: trimmed,
          url: `#/${fileName}?id=${slugify(heading)}`,
          level: 3
        });
      } else if (trimmed.length > 10 && !trimmed.startsWith('|') && !trimmed.startsWith('-')) {
        // Index content (skip tables and short lines)
        const words = trimmed.toLowerCase().split(/\s+/).filter(w => w.length > 3);
        if (words.length > 0) {
          index.push({
            title: currentSection,
            heading: currentSection,
            text: trimmed.substring(0, 150),
            url: `#/${fileName}`,
            level: 0
          });
        }
      }
    });
  });

  return index;
}

function slugify(text) {
  return text.toLowerCase()
    .replace(/[^\w\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .trim();
}

function generateSidebar(content) {
  const lines = content.split('\n');
  const sidebar = [];

  lines.forEach(line => {
    const h1 = line.match(/^#\s+(.+)$/);
    const h2 = line.match(/^##\s+(.+)$/);
    const h3 = line.match(/^###\s+(.+)$/);

    if (h1) {
      sidebar.push(`* [${h1[1]}](#${slugify(h1[1])})`);
    } else if (h2) {
      sidebar.push(`  * [${h2[1]}](#${slugify(h2[1])})`);
    } else if (h3) {
      sidebar.push(`    * [${h3[1]}](#${slugify(h3[1])})`);
    }
  });

  return sidebar.join('\n');
}

